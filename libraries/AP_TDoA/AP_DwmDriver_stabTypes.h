#pragma once

#include <stdint.h>

struct vec3_s {
  uint32_t timestamp; // Timestamp when the data was computed

  float x;
  float y;
  float z;
};

typedef struct vec3_s point_t;


typedef struct tdoaMeasurement_s {
  point_t anchorPosition[2];
  float distanceDiff;
  float stdDev;
  // added info
  uint8_t idA;
  uint8_t idB;
  uint64_t systime_us;
} tdoaMeasurement_t;